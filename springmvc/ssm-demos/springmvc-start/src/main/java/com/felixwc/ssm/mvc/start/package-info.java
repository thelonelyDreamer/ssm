package com.felixwc.ssm.mvc.start;

/*
* java 开源框架, spring Framework的一个独立模块
* MVC架构 在项目中开辟MVC层次结构
* 对控制器中的功能包装，简化 功能架构在工厂只上
* MVC(Model View Controller): 模型 视图 控制器
* 模型：即业务模式，负责完成业务中的数据通信处理 对应项目中的service和dao
* 视图：渲染数据，生成页面，对应项目中的jsp
* 控制器：直接对接请求，控制MVC流程，调整模型，选择视图，对应项目中的servlet
*
* 目的：  降低项目的耦合度，便于项目维护
*
*
*
* */